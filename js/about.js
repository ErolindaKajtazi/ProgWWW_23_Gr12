function readMore2() {
  var partToHide = document.getElementById("container2");
  var changePhoto = document.getElementById("image2");
  if (partToHide.style.display === "block") {
    partToHide.style.display = "none";
    changePhoto.src = "../assets/down-arrow.png";
  } else {
    partToHide.style.display = "block";
    changePhoto.src = "../assets/up-arrow.png";
  }
}

